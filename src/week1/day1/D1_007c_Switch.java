package week1.day1;

public class D1_007c_Switch {


	public static void main(String[] args) {

		String today = "Sanday";

		switch (today) {

		case "Monday":
			System.out.println("Today u should wear pink");
			break;

		case "Tuesday":
			System.out.println("Today u should wear green");
			break;

		case "Wednesday":
			System.out.println("Today u should wear yellow");
			break;

		case "Thursday":
			System.out.println("Today u should wear white");
			break;

		case "Friday":
			System.out.println("Today u should wear red");
			break;

		case "Saturday":
			System.out.println("Today u should wear orange");
			break;

		case "Sunday":
			System.out.println("Today u should wear black");
			break;

		default:

			System.out.println("Go Bare....");

		}

	}

}
